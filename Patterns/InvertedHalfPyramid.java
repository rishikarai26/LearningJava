public class InvertedHalfPyramid {
    public static void main(String[] args) {
        int n = 4;
        
        for(int i=1; i<=n; i++) {

            //good logic is applied here
            for(int j=n; j>=i; j--) {
                System.out.print(i);
            }
            System.out.println();
            }
            
        }

    }
    
